/**
 * Run the generator with pre-parsed options. Called by commands/e2e.js.
 *
 * @param {{ files?: string[], output?: string, glob?: string|null, help?: boolean }} options
 */
function generate(options) {
	const {
		files = [],
		output = './wp-env-bin/e2e/specs/editor',
		glob = null,
		help = false,
	} = options;

	if (help) {
		console.log(HELP_TEXT);
		process.exit(0);
	}

	const allFiles = [...files];

	if (glob) {
		const { globSync } = require('glob');
		const matched = globSync(glob);
		if (!matched.length) {
			console.warn(`⚠️  No files matched glob pattern: ${glob}`);
		} else {
			console.log(`🔍  Matched ${matched.length} file(s) from glob.\n`);
			allFiles.push(...matched);
		}
	}

	if (!allFiles.length) {
		console.error('❌  No files specified. Use --file= or --glob=');
		console.error('   Run with --help for usage information.');
		process.exit(1);
	}

	const uniqueFiles = [...new Set(allFiles)];
	let passed = 0;
	let failed = 0;

	for (const file of uniqueFiles) {
		console.log(`🔧 Processing: ${file}`);
		if (processFile(file, output)) passed++;
		else failed++;
	}

	console.log(`${passed} generated, ${failed} failed.`);
	if (failed > 0) process.exit(1);
}

if (require.main === module) {
	generate(parseArgs(process.argv.slice(2)));
}

module.exports = { generate, generateTestFile };
